#include "push_swap.h"

int	is_valid_number(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '+' || str[i] == '-')
		i++;
	if (!str[i])
		return (0);
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}

int	has_duplicates(int *arr, int size)
{
	int	i;
	int	j;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (arr[i] == arr[j])
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}

void	free_split(char **split)
{
	int	i;

	if (!split)
		return ;
	i = 0;
	while (split[i])
	{
		free(split[i]);
		i++;
	}
	free(split);
}

int	count_numbers(char **av, int start)
{
	int		total;
	char	**pieces;
	int		j;

	total = 0;
	while (av[start])
	{
		if (ft_strchr(av[start], ' '))
		{
			pieces = ft_split(av[start], ' ');
			j = 0;
			while (pieces[j++])
				total++;
			free_split(pieces);
		}
		else
			total++;
		start++;
	}
	return (total);
}
