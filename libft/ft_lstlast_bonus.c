#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst)
	{
		if (lst->next != NULL)
			lst = lst->next;
		else
			return (lst);
	}
	return (lst);
}
